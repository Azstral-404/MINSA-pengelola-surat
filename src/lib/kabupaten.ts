/**
 * Convert kabupaten name to uppercase for header use
 * "Kota Langsa" → "KOTA LANGSA"
 * "Langsa" → "LANGSA"
 */
export function kabupatenToUpper(name: string): string {
  return name.toUpperCase();
}

/**
 * Convert kabupaten to title case for sub-header use
 * "KOTA LANGSA" → "Kota Langsa"
 */
export function kabupatenToTitleCase(name: string): string {
  return name
    .toLowerCase()
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

/**
 * Extract short name for kabupaten (strip "Kota " or "Kabupaten " prefix)
 * "Kota Langsa" → "Langsa"
 * "Aceh Besar" → "Aceh Besar"
 */
export function kabupatenShortName(name: string): string {
  return name.replace(/^(Kota|Kabupaten)\s+/i, '').trim();
}
